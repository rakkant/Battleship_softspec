package game;

public class Player {
	private String name;
	private String symbolValue;
	
	public Player(){
		
	}
	
	public Player(String name,String symbolValue){
		this.name = name;
		this.symbolValue = symbolValue;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
	public String getSymbolValue() {
		return symbolValue;
	}
	
	
}

